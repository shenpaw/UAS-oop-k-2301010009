    private void Konfirmasi_btn1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_Konfirmasi_btn1ActionPerformed
        try (java.sql.Connection conn = new controller.Koneksi().koneksi();
            java.sql.PreparedStatement ps = conn.prepareStatement("UPDATE transaksi SET status = ?, tanggal_tebus = ?, denda = ?, bunga = ?, total_tebusan = ? WHERE id = ?")) {

            ps.setString(1, "Ditebus");
            ps.setString(2, TanggalPenebusan_FormattedTextField.getText());
            ps.setDouble(3, RupiahHelper.parse(Denda_textField.getText()));
            ps.setDouble(4, RupiahHelper.parse(Bunga_textField.getText()));
            ps.setDouble(5, RupiahHelper.parse(Total_textField.getText()));

            ps.setInt(6, idTransaksi);

            ps.executeUpdate();
            javax.swing.JOptionPane.showMessageDialog(this, "Barang berhasil ditebus!");
            new DaftarTransaksi().setVisible(true);
            this.dispose();
        } catch (Exception e) {
            javax.swing.JOptionPane.showMessageDialog(this, "Gagal menebus: " + e.getMessage());
        }
    }//GEN-LAST:event_Konfirmasi_btn1ActionPerformed

