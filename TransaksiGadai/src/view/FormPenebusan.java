    private void hitungDendaDanTotal() {
        try {
            java.time.LocalDate jatuh = java.time.LocalDate.parse(JatuhTempo_FormattedTextField2.getText());
            java.time.LocalDate hariIni = java.time.LocalDate.now();

            long selisihHari = java.time.temporal.ChronoUnit.DAYS.between(jatuh, hariIni);
            double denda = selisihHari > 0 ? selisihHari * 1000 : 0;

            double pinjaman = RupiahHelper.parse(Pinjaman_textField.getText());

            double bunga = pinjaman * 0.05; // 5% bunga
            double total = pinjaman + bunga + denda;

            Denda_textField.setText(RupiahHelper.format(denda));
            Bunga_textField.setText(RupiahHelper.format(bunga));
            Total_textField.setText(RupiahHelper.format(total));

        } catch (Exception e) {
            javax.swing.JOptionPane.showMessageDialog(this, "Gagal menghitung: " + e.getMessage());
        }
    }

